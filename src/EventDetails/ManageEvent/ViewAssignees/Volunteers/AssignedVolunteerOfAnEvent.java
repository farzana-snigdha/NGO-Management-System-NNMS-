package EventDetails.ManageEvent.ViewAssignees.Volunteers;

public class AssignedVolunteerOfAnEvent {
    String name,email,phone,designation,address,id;

    public AssignedVolunteerOfAnEvent(String name, String email, String phone, String designation, String address, String id) {
        this.name = name;
        this.email = email;
        this.phone = phone;
        this.designation = designation;
        this.address = address;
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getDesignation() {
        return designation;
    }

    public void setDesignation(String designation) {
        this.designation = designation;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }
}
